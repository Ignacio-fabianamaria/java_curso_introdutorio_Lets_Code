package lacos_de_repeticao;

public class index {
    public static void main(String[] args) {
        // tabuada de "X"

        for (int i = 1; i <= 10; i++) {
            for (int j = 1; j <=10; j++) {
                System.out.println(j+ " X " + i+ " = " + j * i);
            }
        }
    }
}
